uint64_t
f ()
{
  const size_t length = strlen (number);
  uint8_t *digits = calloc (length, 1);
  for (size_t i = 0; i < length; i++)
    {
      digits[i] = number[i] - '0';
    }

  // Following initializations rely on
  // that the first 13 digits have no 0s.
  uint64_t rolling = 1;
  for (size_t i = 0; i < 13; i++)
    {
      rolling *= digits[i];
    }
  uint64_t maximum = rolling;
  uint8_t zeros = 0;

  for (size_t i = 13; i < length; i++)
    {
      uint8_t head = digits[i-13];
      uint8_t tail = digits[i];

      if (head != 0)
        rolling /= head;

      if (tail != 0)
        rolling *= tail;

      zeros += (tail == 0) - (head == 0);
      if (maximum < rolling && zeros == 0)
        maximum = rolling;
    }

  free (digits);
  return maximum;
}

